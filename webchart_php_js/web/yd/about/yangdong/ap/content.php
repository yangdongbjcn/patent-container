		<h2>AP</h2>
		<h3>About affinity propagation</h3>
		<p><a href="http://www.psi.toronto.edu/index.php?q=affinity%20propagation">Affinity propagation (AP) clustering algorithm </a>extracts center points by initially viewing all points as potential center points and iteratively removing impossible points according to the similarity of every two points and preference of every point.</p>

		<h3>Papers of Arts</h3>
		<p>Frey, B.J., Dueck, D.: Clustering by Passing Messages between Data Points. Science 315, 972--976 (2007). Affinity propagation algorithm is proposed.</p>
		<p>PerformanceDueck, D., Frey, B.J.: Non-metric Affinity Propagation for Unsupervised Image Categorization. In: IEEE International Conf. on Computer Vision, pp. 1--8. IEEE Press, New York (2007). 
		Affinity propagation algorithm is adopted for image categorization.</p>
		<p>Zhang, X.L., Furtlehner, C., Sebag, M.: Data Streaming with Affinity Propagation. In: Machine Learning and Knowledge Discovery in Databases, pp. 628--643. Springer, Heidelberg (2008).
		Weighted affinity propagation algorithm is proposed.</p>
		<p>Furtlehner, C., Sebag, M., Zhang, X.L.: Scaling Analysis of Affinity Propagation. Phys. Rev. E 81(6), 006102 (2010).
		Scaling ability of affinity propagation algorithm is analyzed.</p>

